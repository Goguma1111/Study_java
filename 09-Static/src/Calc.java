public class Calc {
    // 계산기 기능을 갖는 클래스
    public int plus(int x, int y) {
        return x + y;
    }

    public int minus (int x, int y){
        return x - y;
    }

    public int times (int x, int y){
        return x * y;
    
    }

    public int divied (int x , int y){
        return x / y;
    }

}

