public class Ex06_Ifelse {
    public static void main(String[] args) {
        //boolean iskorean = true;
        boolean iskorean = false;

        if (iskorean) {
            System.out.println("한국사람입니다.");
        } else {
            System.out.println("한국사람이 아닙니다.");
        }
        
        if(!iskorean) {
            System.out.println("한국사람이 아닙니다.");
        }
    
        else {
            System.out.println("한국사람입니다.");
        }
    }
}
